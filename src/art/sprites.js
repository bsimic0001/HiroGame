// hd: hi-res finishing — rounded contours + top-lit shading
const hd = (cv) => shade(scale2x(cv));
// Hiro frames are native hi-res: outline + shading only (no upscale)
function frames(a, b, swap) { return [shade(outline(compile(a, swap))), shade(outline(compile(b, swap)))]; }

export function makeActor(swap = null) {
  const left = frames(HIRO_LEFT_A, HIRO_LEFT_B, swap);
  return {
    down: frames(HIRO_DOWN_A, HIRO_DOWN_B, swap),
    up: frames(HIRO_UP_A, HIRO_UP_B, swap),
    left,
    right: left.map(mirror),
  };
}

